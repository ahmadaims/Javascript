/* Addition operator */
let x = 10;
let y = 20;
console.log(`x + y = ${x + y}`);
console.log(`y - x = ${y - x}`);
console.log(`x * y = ${x * y}`);
console.log(`y / x = ${y / x}`);


/* Reassigning the variable value */
/* The modulus operator (%) returns the division remainder. */
x = 29;
y = 10;
console.log(`x % y = ${x % y}`);


/*Increament operator */
x = 10;
x++;
console.log(`x = ${x}`);
/* Decreament Operator */
x = 5;
x--;
console.log(`x = ${x}`);



/* Exponentiation Operator */
/* The exponentiation operator (**) raises the first operand to the power of the second operand. */
a = 5;
b = 2;
console.log(`a**b = ${a**b}`);